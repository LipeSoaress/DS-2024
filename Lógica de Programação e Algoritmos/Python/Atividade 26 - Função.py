def soma(n1, n2):
    return (n1+n2)

def subtração(n1, n2):
    return (n1-n2)

def Multiplicação(n1, n2):
    return (n1*n2)

def Divisão(n1, n2):
    return (n1/n2)
print("--------------------Divisão-----------------------")
print(Divisão(34.5, 700))

print("-------------------soma------------------------")
print(soma(70, 40))

print("------------------subtração-------------------------")
print(subtração(340, 320))

print("-------------------Multiplicação------------------------")
print(Multiplicação(2, 4))
